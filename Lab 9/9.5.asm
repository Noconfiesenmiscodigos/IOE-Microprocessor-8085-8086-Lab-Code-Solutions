;Write a program to add ten 16-bit numbers stored in memory and store and display the result in decimal format.

TITLE SUM 10 15 BIT NUMBER FORM MEMORY
.MODEL SMALL
.DATA
NUMS DW 100, 200, 300, 400, 500, 600, 700, 800, 900, 1000
SUMS DW ?
.STACK
.CODE

MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

;; CALCULATING THE SUM
MOV CX, 10
LEA BX, NUMS
MOV AX, 00

L2: ADD AX, [BX]
INC BX
INC BX
LOOP L2
MOV SUMS, AX

;; CONVERTING TO DECIMAL
MOV CX, 00
MOV BX, 10

L3: MOV DX, 00
INC CX
DIV BX
CMP AX, 00
PUSH DX
JG L3

;; PRINTING IN DECIMAL
DIS: POP DX
OR DX, 30H
MOV AH, 02H
INT 21H
LOOP DIS

;; ENDING PROGRAM
MOV AH, 4CH
INT 21H
MAIN ENDP

END MAIN
