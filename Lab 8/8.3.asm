;Write an assembly language program that takes a string (having 24 characters at max.) from the user and display each character at the center of each line.

.MODEL SMALL
.DATA
STRLEN DB 24
STRSZ DB ?
STR DB 24 DUP('$')
STRTRM DB '$'
.STACK
.CODE

MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

;; READING STRING
LEA DX, STRLEN
MOV AH, 0AH
INT 21H

;; CLEARING THE SCREEN
MOV AH, 00H
MOV AL, 03H
INT 10H

;; DISPLAYING IN THE CENTER
;; SETTING CURSOR POSITION
LEA SI, STR
MOV CH, 00
MOV CL, STRSZ
MOV DX, 00H

L1: MOV BH, 00H
MOV DL, 40
MOV AH, 02H
INT 10H

;; DISPLAYING IN CURSOR POSITION
MOV DL, [SI]
MOV AH, 02H
INT 21H
INC DH
INC SI
LOOP L1

;; ENDING THE PROGRAM
MOV AX, 4C00H
INT 21H

MAIN ENDP
END MAIN
