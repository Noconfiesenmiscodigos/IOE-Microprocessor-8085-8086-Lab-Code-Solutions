;Write a program to read a string and display each word in separate lines.

TITLE WORD
.MODEL SMALL
.DATA
STRLEN DB 20
STRSZ DB 00
STR DB 20 DUP(' ')
STRTRM DB '$'
.STACK
.CODE

MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

;; READING STRING
LEA DX, STRLEN
MOV AH, 0AH
INT 21H

;; NEW LINE CHARACTER
MOV DL, 0DH
MOV AH, 02H
INT 21H
MOV DL, 0AH
MOV AH, 02H
INT 21H

;; PRINTING WORD
MOV CX, 00H
MOV CL, STRLEN
LEA BX, STR
L1: MOV DL, [BX]
CMP DL, ' '
JNE EX

;; NEW LINE CHARACTER
MOV DL, 0DH
MOV AH, 02H
INT 21H
MOV DL, 0AH
MOV AH, 02H
INT 21H
JMP BRK

EX: MOV AH, 02H
INT 21H

BRK: INC BX
LOOP L1

;; ENDING PROGRAM
MOV AX, 4C00H
INT 21H

MAIN ENDP
END MAIN
