;Write an assemble language program in DEBUG to add four 16-bit numbers and test it.

.MODEL SMALL
.STACK 64
.DATA
VALUE DW  2555H, 1530H, 8743H, 9853H
.CODE
	
MOV AX,@DATA
MOV DS,AX
MOV BX,0000H
MOV AX,0000H
MOV CX,0004H 
LEA DI,VALUE    
	
AGAIN: ADD BX,[DI]
JNC NOCARRY
INC AX
	
NOCARRY: ADD DI,2H
DEC CX
JNZ AGAIN
MOV [DI], BX
ADD DI,2H
MOV [DI], AX
MOV AH,4CH
INT 21H   

END
	
